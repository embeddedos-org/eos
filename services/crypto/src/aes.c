static const uint8_t rcon[11] = {0x00,0x01,0x02,0x04,0x08,0x10,0x20,0x40,0x80,0x1b,0x36};

#define GETU32(p) ((uint32_t)(p)[0]<<24|(uint32_t)(p)[1]<<16|(uint32_t)(p)[2]<<8|(uint32_t)(p)[3])

static uint32_t sub_word(uint32_t w) {
    return ((uint32_t)sbox[(w>>24)&0xff]<<24)|((uint32_t)sbox[(w>>16)&0xff]<<16)|
           ((uint32_t)sbox[(w>>8)&0xff]<<8)|((uint32_t)sbox[w&0xff]);
}

static uint32_t rot_word(uint32_t w) {
    return (w<<8)|(w>>24);
}

void eos_aes_init(EosAesCtx *ctx, const uint8_t *key, int key_bits) {
    int nk = key_bits / 32;
    ctx->nr = nk + 6;

    for (int i = 0; i < nk; i++)
        ctx->rk[i] = GETU32(key + 4*i);

    int total = 4 * (ctx->nr + 1);
    for (int i = nk; i < total; i++) {
        uint32_t t = ctx->rk[i-1];
        if (i % nk == 0)
            t = sub_word(rot_word(t)) ^ ((uint32_t)rcon[i/nk] << 24);
        else if (nk > 6 && i % nk == 4)
            t = sub_word(t);
        ctx->rk[i] = ctx->rk[i-nk] ^ t;
    }
}

void eos_aes_encrypt_block(const EosAesCtx *ctx,
                           const uint8_t in[16], uint8_t out[16]) {
    uint8_t s[16];
    memcpy(s, in, 16);

    /* AddRoundKey */
    for (int i=0;i<16;i++) s[i] ^= (uint8_t)(ctx->rk[i/4] >> (24-8*(i%4)));

    for (int r=1;r<=ctx->nr;r++) {
        uint8_t t[16];
        /* SubBytes */
        for (int i=0;i<16;i++) t[i] = sbox[s[i]];

        /* ShiftRows */
        s[0]=t[0];s[1]=t[5];s[2]=t[10];s[3]=t[15];
        s[4]=t[4];s[5]=t[9];s[6]=t[14];s[7]=t[3];
        s[8]=t[8];s[9]=t[13];s[10]=t[2];s[11]=t[7];
        s[12]=t[12];s[13]=t[1];s[14]=t[6];s[15]=t[11];

        /* MixColumns (skip on last round) */
        if (r < ctx->nr) {
            for (int c=0;c<4;c++) {
                int i=c*4;
                uint8_t a0=s[i],a1=s[i+1],a2=s[i+2],a3=s[i+3];
                uint8_t r0=(uint8_t)(a0^a1^a2^a3);
                uint8_t t0,t1,t2,t3;
                t0=a0^a1; t0=((t0<<1)^((t0&0x80)?0x1b:0));
                t1=a1^a2; t1=((t1<<1)^((t1&0x80)?0x1b:0));
                t2=a2^a3; t2=((t2<<1)^((t2&0x80)?0x1b:0));
                t3=a3^a0; t3=((t3<<1)^((t3&0x80)?0x1b:0));
                s[i]  =a0^t0^r0;
                s[i+1]=a1^t1^r0;
                s[i+2]=a2^t2^r0;
                s[i+3]=a3^t3^r0;
            }
        }

        /* AddRoundKey */
        for (int i=0;i<16;i++) s[i] ^= (uint8_t)(ctx->rk[r*4+i/4] >> (24-8*(i%4)));
    }
    memcpy(out, s, 16);
}

void eos_aes_decrypt_block(const EosAesCtx *ctx,
                           const uint8_t in[16], uint8_t out[16]) {
    uint8_t s[16];
    memcpy(s, in, 16);

    /* AddRoundKey (last round key) */
    for (int i=0;i<16;i++) s[i] ^= (uint8_t)(ctx->rk[ctx->nr*4+i/4] >> (24-8*(i%4)));

    for (int r=ctx->nr-1;r>=0;r--) {
        /* InvShiftRows */
        uint8_t t[16];
        t[0]=s[0];t[5]=s[1];t[10]=s[2];t[15]=s[3];
        t[4]=s[4];t[9]=s[5];t[14]=s[6];t[3]=s[7];
        t[8]=s[8];t[13]=s[9];t[2]=s[10];t[7]=s[11];
        t[12]=s[12];t[1]=s[13];t[6]=s[14];t[11]=s[15];

        /* InvSubBytes */
        for (int i=0;i<16;i++) s[i] = rsbox[t[i]];

        /* AddRoundKey */
        for (int i=0;i<16;i++) s[i] ^= (uint8_t)(ctx->rk[r*4+i/4] >> (24-8*(i%4)));

        /* InvMixColumns (skip on round 0) */
        if (r > 0) {
            for (int c = 0; c < 4; c++) {
                int ii = c * 4;
                uint8_t a0 = s[ii], a1 = s[ii+1], a2 = s[ii+2], a3 = s[ii+3];

                /* xtime: multiply by 2 in GF(2^8) */
                #define XT(x) ((uint8_t)(((x)<<1) ^ (((x)&0x80) ? 0x1b : 0)))

                /* Multiply by n in GF(2^8) using xtime */
                #define MUL2(x) XT(x)
                #define MUL4(x) XT(XT(x))
                #define MUL8(x) XT(XT(XT(x)))
                #define MUL9(x)  ((uint8_t)(MUL8(x) ^ (x)))
                #define MUL11(x) ((uint8_t)(MUL8(x) ^ MUL2(x) ^ (x)))
                #define MUL13(x) ((uint8_t)(MUL8(x) ^ MUL4(x) ^ (x)))
                #define MUL14(x) ((uint8_t)(MUL8(x) ^ MUL4(x) ^ MUL2(x)))

                s[ii]   = (uint8_t)(MUL14(a0) ^ MUL11(a1) ^ MUL13(a2) ^ MUL9(a3));
                s[ii+1] = (uint8_t)(MUL9(a0)  ^ MUL14(a1) ^ MUL11(a2) ^ MUL13(a3));
                s[ii+2] = (uint8_t)(MUL13(a0) ^ MUL9(a1)  ^ MUL14(a2) ^ MUL11(a3));
                s[ii+3] = (uint8_t)(MUL11(a0) ^ MUL13(a1) ^ MUL9(a2)  ^ MUL14(a3));

                #undef XT
                #undef MUL2
                #undef MUL4
                #undef MUL8
                #undef MUL9
                #undef MUL11
                #undef MUL13
                #undef MUL14
            }
        }
    }
    memcpy(out, s, 16);
}

void eos_aes_cbc_encrypt(const EosAesCtx *ctx, const uint8_t *iv,
                         const uint8_t *in, uint8_t *out, size_t len) {
    uint8_t prev[16];
    memcpy(prev, iv, 16);
    for (size_t off = 0; off + 16 <= len; off += 16) {
        uint8_t block[16];
        for (int i=0;i<16;i++) block[i] = in[off+i] ^ prev[i];
        eos_aes_encrypt_block(ctx, block, out + off);
        memcpy(prev, out + off, 16);
    }
}

void eos_aes_cbc_decrypt(const EosAesCtx *ctx, const uint8_t *iv,
                         const uint8_t *in, uint8_t *out, size_t len) {
    uint8_t prev[16];
    memcpy(prev, iv, 16);
    for (size_t off = 0; off + 16 <= len; off += 16) {
        uint8_t block[16];
        eos_aes_decrypt_block(ctx, in + off, block);
        for (int i=0;i<16;i++) out[off+i] = block[i] ^ prev[i];
        memcpy(prev, in + off, 16);
    }
}
